    private void jbtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBuscarActionPerformed
        Cliente cliente = null;
        
        if(!jtxtRut.getText().isBlank()){
            int rut = Integer.parseInt(jtxtRut.getText());
            cliente = DAOCliente.existeCliente(rut);
            if(cliente != null){
                //jlblNombre.setText(cliente.getNombre() + " " + cliente.getCorreo());
                jtxtRut.setEnabled(false);
            }else{
                JOptionPane.showMessageDialog(null,"Cliente no existe " + rut);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Debe ingresart el rut!!!");
        }
        
    }//GEN-LAST:event_jbtnBuscarActionPerformed

    private void jbtnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnLimpiarActionPerformed
        limpiarCampos();
    }//GEN-LAST:event_jbtnLimpiarActionPerformed

    private void llenarVehiculos(){
        jcbcVehiculos.addItem("Seleccionar");
        ArrayList<String> lista = DAOVehiculo.getVehiculos();
        for (String item : lista) {
            jcbcVehiculos.addItem(item);
        }
    }
    
    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        limpiarCampos();
        jcbcVehiculos.removeAllItems();
        llenarVehiculos();
    }//GEN-LAST:event_formComponentShown

    private Venta getVenta(){
        Venta venta = null;
        Cliente cliente = null;
        Vehiculo vehiculo = null;

        if(validarCampos()){
            int rut = Integer.parseInt(jtxtRut.getText());
            cliente = DAOCliente.existeCliente(rut);
            
            String patente = (String)jcbcVehiculos.getSelectedItem();
            vehiculo = DAOVehiculo.existeVehiculo(patente);
            
            double precio = Double.parseDouble(jtxtValorVenta.getText());
            venta = new Venta(cliente, vehiculo, precio);
        }
        return venta;
    }
        
    private void jbtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnConfirmarActionPerformed
        Venta venta = getVenta();
        if(venta != null){
            if(DAOVenta.ingresar(venta)){
                JOptionPane.showMessageDialog(null,"Venta registrada ok. ");
                limpiarCampos();
            }else{
                JOptionPane.showMessageDialog(null,"Venta no registrada");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Todos los campos son obligatorios!!");
        }
    }//GEN-LAST:event_jbtnConfirmarActionPerformed


