    private boolean validarCampos(){
        return !(jtxtRut.getText().isBlank() ||
                jtxtValorVenta.getText().isBlank() ||
                jcbcVehiculos.getSelectedIndex() == 0
        );       
    }

    private void limpiarCampos(){
        jtxtRut.setEnabled(true);
        jtxtRut.setText("");
        jtxtValorVenta.setText("");
        jcbcVehiculos.setSelectedIndex(0);
    }
    
