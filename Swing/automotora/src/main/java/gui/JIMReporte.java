    private void limpiarCampos(){
        jtxtAnio.setEnabled(true);
        jtxtAnio.setText("");
        jrbConsulta1.setSelected(false);
        jrbConsulta2.setSelected(false);
        jrbConsulta3.setSelected(false);
        jtxaReportes.setText("");
    }
    
    private boolean validarSeleccion(){
        if(!jrbConsulta1.isSelected() || jtxtAnio.getText().isBlank()){
            return (
                    jrbConsulta2.isSelected() ||
                    jrbConsulta3.isSelected()
                    );    
        }else{
            return true;
        }
    }
    
    private void jbtnEjecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEjecutarActionPerformed
        
        double venta = 0;
        jtxaReportes.setText("");
        
        if(validarSeleccion()){
            if(jrbConsulta1.isSelected()){
                int anio = Integer.parseInt(jtxtAnio.getText());
                venta = DAOVenta.valorVentasAnio(anio);
                jtxaReportes.setText("El total de venta para el a침o \n" + anio + " fue de "+ venta);
            }else if(jrbConsulta2.isSelected()){
                ArrayList<String> lista = DAOVenta.vehiculoMasVendidos();
                if(!lista.isEmpty()){
                    for (String registro : lista) {
                        jtxaReportes.append(registro);
                    }
                }else{
                    jtxaReportes.append("No existe registro de ventas!!!");
                }
            }else if(jrbConsulta3.isSelected()){
                ArrayList<String> lista = DAOVenta.clientesMasCompras();
                if(!lista.isEmpty()){
                    for (String registro : lista) {
                        jtxaReportes.append(registro);
                    }
                }else{
                    jtxaReportes.append("No existe registro de ventas!!!");
                }
            }
        }else{
            JOptionPane.showMessageDialog(null,"Todos los campos son obligatorios!!");
        }

    }//GEN-LAST:event_jbtnEjecutarActionPerformed

