    private void jbtn_modificarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_modificarClienteActionPerformed
        
        
        int rut = Integer.parseInt(jtxt_rut.getText());
        String nombre = jtxt_nombre.getText();
        int edad = Integer.parseInt(jtxt_edad.getText());
        
        Cliente cli = new Cliente(rut,nombre,edad);
        if (!DAOCliente.agregarCliente(cli)){
            JOptionPane.showMessageDialog(null,"El Cliente ya exist√≠a");
        } else{
            JOptionPane.showMessageDialog(null,"El Cliente fue agregado");
        }
        
        limpiarCampos();
    }//GEN-LAST:event_jbtn_modificarClienteActionPerformed

    private void limpiarCampos(){
        this.jtxt_rut.setText("");
        this.jtxt_rut.setText("");
        this.jtxt_edad.setText("");
    }
    private void jbtn_retrocederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_retrocederActionPerformed
        limpiarCampos();
    }//GEN-LAST:event_jbtn_retrocederActionPerformed

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
       
        if(DAOCliente.esVacia()){
            this.jbtn_retroceder.setEnabled(false);
            this.jbtn_avanzar.setEnabled(false);
            this.jbtn_modificarCliente.setEnabled(false);
            this.jlbl_contador.setText("Registro: " + contador);
        }else{
            contador++;
            Cliente cli = DAOCliente.traerPrimerCliente();
            this.jtxt_rut.setText(String.valueOf(cli.getRut()));
            this.jtxt_nombre.setText(cli.getNombre());
            this.jtxt_edad.setText(String.valueOf(cli.getEdad()));
            this.jlbl_contador.setText("Registro: " + contador);
        }
    }//GEN-LAST:event_formComponentShown


