    private void jbtnCalcular1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCalcular1ActionPerformed
        double nota1 = 0;
        double nota2 = 0;
        double nota3 = 0;
        int porc1 = 0;
        int porc2 = 0;
        int porc3 = 0;
        
        if( validarAsignatura() ){

            try {
                nota1 = Double.parseDouble(this.jtxt_Nota1.getText());
                nota2 = Double.parseDouble(this.jtxt_Nota2.getText());
                nota3 = Double.parseDouble(this.jtxt_Nota3.getText());

                porc1 = Integer.parseInt(this.jtxt_porc1.getText());
                porc2 = Integer.parseInt(this.jtxt_porc2.getText());
                porc3 = Integer.parseInt(this.jtxt_porc3.getText());
                
                double notaPromedio = redondear( (nota1*porc1/100) + (nota2*porc2/100) + (nota3*porc3/100) );
                
                if(validarPorcentaje(porc1, porc2, porc3)){
                    if(validarRangoNotas(nota1, nota2, nota3)){
                        //Se debe realizar explicitamente esta conversiÃ³nd de tipos de double a String
                        String notaPromedioS = String.valueOf(notaPromedio);
                        this.jlbl_promedio.setText(notaPromedioS);
                        calcularEstado(notaPromedio);
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "Las notas deben estar en el rago de 1-7");
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Los ponderadores no suman 100%");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "Un dato no pudo ser convertido a numero!!!");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar una asignatura");
        }
    }//GEN-LAST:event_jbtnCalcular1ActionPerformed

    private boolean validarPorcentaje(int porc1, int porc2, int porc3){
        return (porc1+porc2+porc3) == 100;
    }
    private void calcularEstado(double notaFinal){
        if(notaFinal >= 4){
            this.jlbl_EstadoAlumno.setText("Aprobado");
            this.jlbl_EstadoAlumno.setForeground(Color.green);
        } else{
            this.jlbl_EstadoAlumno.setText("Reprobado");
            this.jlbl_EstadoAlumno.setForeground(Color.RED);
        }
    }
    private boolean validarAsignatura(){
        return this.jcb_Asignatura.getSelectedIndex() > 0;
    }
    
    private boolean validarRangoNotas(double nota1, double nota2, double nota3){

        if(nota1 >=1 && nota1 <=7){
            if(nota2 >= 1 && nota2 <=7 ){
                if(nota3 >=1 && nota3 <=7 ){
                    return true;
                }
            }
            
        }
        return false;
    }
    
    private static double redondear(double nota) {
        return Math.round(nota * 100.0)/100.0; //redondeo a 2 decimales^M
    }
    
