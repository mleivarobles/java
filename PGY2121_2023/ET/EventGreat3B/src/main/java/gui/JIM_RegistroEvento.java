    private void llenarVehiculos(){
        this.jcbxEventos.removeAllItems();
        jcbxEventos.addItem("Seleccionar");
        ArrayList<String> lista = DAOEvento.listarEquipos();
        for (String item : lista) {
            jcbxEventos.addItem(item);
        }
    }
    
