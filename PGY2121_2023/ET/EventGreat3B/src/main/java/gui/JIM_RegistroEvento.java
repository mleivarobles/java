    private boolean validarCampos(){
        return !(jtxtRut.getText().isBlank() ||
                jcbxEventos.getSelectedIndex() == 0
                );
    }
    
    private void limpiarCampos(){
        this.jtxtRut.setEnabled(true);
        this.jcbxEventos.setEnabled(false);
        this.jtxtRut.setText("");
        this.jcbxEventos.setSelectedIndex(0);
    }
    
    private void jbtbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtbBuscarActionPerformed
       
        if(!this.jtxtRut.getText().isBlank()){
            int rut = Integer.parseInt(jtxtRut.getText());
            Cliente cliente = DAOCliente.existeCliente(rut);
            
            if( cliente != null ){
                this.jtxtRut.setEnabled(false);
                this.jcbxEventos.setEnabled(true);
            }else{
                JOptionPane.showMessageDialog(null,"El cliente no existe " + rut);
            }
        } else{
            JOptionPane.showMessageDialog(null,"Debe ingresar el rut!!");
        }
    }//GEN-LAST:event_jbtbBuscarActionPerformed

    private RegistroEvento getRegistroEvento(){
        if (validarCampos()){
            int rut = Integer.parseInt(jtxtRut.getText());
            Cliente cliente = DAOCliente.existeCliente(rut);
            
            String codigo = (String)jcbxEventos.getSelectedItem();
            Evento evento = DAOEvento.existeEquipo(codigo);
            
            RegistroEvento venta = new RegistroEvento(cliente,evento);
            return venta;
        }
        return null;
    }
    
    private void jbtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnConfirmarActionPerformed
        RegistroEvento venta = getRegistroEvento();
        
        if (venta != null){
            if(DAORegistroEvento.ingresar(venta)){
                JOptionPane.showMessageDialog(null,"Registro de ventas ingresado OK: ");
                limpiarCampos();
            }else{
                JOptionPane.showMessageDialog(null,"Venta no ingresado ");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Todos los campos son obligatorios!!");
        }
    }//GEN-LAST:event_jbtnConfirmarActionPerformed

