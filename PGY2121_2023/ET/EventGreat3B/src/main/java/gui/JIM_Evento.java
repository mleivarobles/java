    private boolean validarCampos(){
        return !(
                jtxtDescripcion.getText().isBlank() ||
                jtxtAsistentes.getText().isBlank() || 
                jtxtRecinto.getText().isBlank() || 
                jtxtNroMesas.getText().isBlank() ||
                jtxtPrecio.getText().isBlank() || 
                jtxtAtributo2.getText().isBlank());
    }
    
    private void limpiarCampos(){
        jtxtCodigo.setEnabled(true);
        jcbTipoEvento.setSelectedIndex(0);
        jtxtCodigo.setText("");
        jtxtDescripcion.setText("");
        jtxtAsistentes.setText("");
        jtxtRecinto.setText("");
        jtxtNroMesas.setText("");
        jtxtPrecio.setText("");
        jcbxAtributo1.setSelected(false);
        jtxtAtributo2.setText("");
    }
    
    private Evento getEvento(String codigo){
        
        try {
            if (validarCampos()){
                String descripcion = this.jtxtDescripcion.getText();
                int nroAsistentes = Integer.parseInt(this.jtxtAsistentes.getText());
                int recinto = Integer.parseInt(this.jtxtRecinto.getText());
                int nroMesas = Integer.parseInt(this.jtxtNroMesas.getText());
                double precio = Double.parseDouble(this.jtxtPrecio.getText());
                
                //fiesta
                boolean tieneAlcohol;
                int nroDj = 0;
                //matrimonio
                boolean tieneMusica;
                int nroPlatos = 0;

                Evento evento = null;
                
                if(this.jcbTipoEvento.getSelectedItem().toString().equalsIgnoreCase("Fiesta")){
                    tieneAlcohol = this.jcbxAtributo1.isSelected();
                    nroDj = Integer.parseInt(this.jtxtAtributo2.getText());
                    evento = new Fiesta(codigo, tieneAlcohol, nroDj, descripcion, 
                            nroAsistentes, recinto, nroMesas, precio );
                }else{
                    tieneMusica = this.jcbxAtributo1.isSelected();
                    nroPlatos = Integer.parseInt(this.jtxtAtributo2.getText());
                    evento = new Matrimonio(codigo, tieneMusica, nroPlatos, descripcion, 
                            nroAsistentes, recinto, nroPlatos, precio );
                }
                return evento;
            }
        } catch (NumberFormatException e) {
            System.out.println("Error de conversion: " + e.toString());
        }
        return null;
    }
    
    private void jbtnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnIngresarActionPerformed
        String codigo = "";
        Evento evento = getEvento(codigo);
        
        if (evento != null){
            if(DAOEvento.ingresar(evento)){
                JOptionPane.showMessageDialog(null,"Evento ingresado OK: " + evento.getCodigo() );
                limpiarCampos();
            }else{
                JOptionPane.showMessageDialog(null,"Evento no ingresado " + evento.getCodigo() );
            }
        }else{
            JOptionPane.showMessageDialog(null,"Todos los campos son obligatorios!!");
        }
    }//GEN-LAST:event_jbtnIngresarActionPerformed

