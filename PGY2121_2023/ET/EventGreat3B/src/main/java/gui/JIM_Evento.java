    private void jbtbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtbBuscarActionPerformed
        
        if(!this.jtxtCodigo.getText().isBlank()){
            String codigo = jtxtCodigo.getText();
            Evento evento = DAOEvento.existeEquipo(codigo);
            
            if( evento != null ){
                this.jtxtCodigo.setEnabled(false);
                
                this.jtxtDescripcion.setText(evento.getDescripcion());
                this.jtxtAsistentes.setText(String.valueOf(evento.getCantidadAsistentes()));
                this.jtxtRecinto.setText(String.valueOf(evento.getTamanioRecinto()));
                this.jtxtNroMesas.setText(String.valueOf(evento.getCantidadMesas()));
                this.jtxtPrecio.setText(String.valueOf(evento.getPrecio()));
                
                if(evento instanceof Fiesta fiesta){
                    this.jcbTipoEvento.setSelectedIndex(0);
                    this.jcbxAtributo1.setSelected(fiesta.isTieneAlcohol());
                    this.jtxtAtributo2.setText(String.valueOf(fiesta.getNroDj()));
                }else if(evento instanceof Matrimonio matrimonio){
                    this.jcbTipoEvento.setSelectedIndex(1);
                    this.jcbxAtributo1.setSelected(matrimonio.isTieneMusica());
                    this.jtxtAtributo2.setText(String.valueOf(matrimonio.getCantPlatos()));
                }
            }else{
                JOptionPane.showMessageDialog(null,"El codigo evento no existe: " + codigo);
            }
        } else{
            JOptionPane.showMessageDialog(null,"Debe ingresar el codigo!!");
        }
    }//GEN-LAST:event_jbtbBuscarActionPerformed

    private void jbtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEditarActionPerformed
        String codigo = "";
        if (!this.jtxtCodigo.getText().isBlank()){
            codigo = this.jtxtCodigo.getText();
        }
        Evento evento = getEvento(codigo);
        
        if(evento != null){
            if(DAOEvento.actualizar(evento)){
                JOptionPane.showMessageDialog(null,"Evento modificado ok: " + evento.getCodigo());            
                this.jtxtCodigo.setEnabled(true);
                limpiarCampos();
            }else{
                JOptionPane.showMessageDialog(null,"Evento no existe: " + evento.getCodigo());
            }
        }else{
            JOptionPane.showMessageDialog(null,"Todos los campos son obligatorios!!");
        }  
    }//GEN-LAST:event_jbtnEditarActionPerformed

    private void jbtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEliminarActionPerformed
        String codigo = "";
        if (!this.jtxtCodigo.getText().isBlank()){
            codigo = this.jtxtCodigo.getText();
        }
        Evento equipo = getEvento(codigo);
         
        if(equipo != null){
            codigo = jtxtCodigo.getText();
            if(DAOEvento.eliminar(codigo)){
                this.jtxtCodigo.setEnabled(true);
                JOptionPane.showMessageDialog(null,"Evento eliminado OK: " + codigo);
                limpiarCampos();
            }else{
                JOptionPane.showMessageDialog(null,"Evento no existe: " + codigo);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Todos los campos son obligatorios!!");
        } 
    }//GEN-LAST:event_jbtnEliminarActionPerformed
    
    private void ocultarCampos(){
        this.jlblAtributo1.setVisible(false);
        this.jlblAtributo2.setVisible(false);
        this.jcbxAtributo1.setVisible(false);
        this.jtxtAtributo2.setVisible(false);
    }
    
    private void validarEquipos(){
        int opcion = this.jcbTipoEvento.getSelectedIndex();
        switch (opcion) {
            //fiesta
            case 0 -> {
                //Laptop
                this.jlblAtributo1.setText("Alcohol?");
                this.jlblAtributo2.setText("#DJ's");
            }
            //matrimonio
            case 1 -> {
                //Desktop
                this.jlblAtributo1.setText("Musica?");
                this.jlblAtributo2.setText("#Platos");
            }
            default -> {
                ocultarCampos();
            }
        }
    }
    

